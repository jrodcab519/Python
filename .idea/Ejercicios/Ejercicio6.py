Cel = float (input("Inserta unos grados Celsius para convertirlos en Fahrenheit: "))

Far = (Cel * 9/5) + 32

def convierteCelsius():
    Cel
    print(f"{Cel} grados Celsius son {Far} grados fahrenheit")

convierteCelsius()

Far = float (input("Inserta unos grados Fahrenheit para convertirlos en Celsius"))

Cel = (Far - 32) * 5/9

def convierteFarenheit():
    Far
    print(f"{Far} grados Fahrenheit son {Cel} grados Celsius")

convierteFarenheit()
